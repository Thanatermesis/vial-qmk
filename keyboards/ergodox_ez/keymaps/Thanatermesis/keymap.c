bool process_record_user(uint16_t keycode, keyrecord_t *record) {
     switch (keycode) {
        case VRSN:
           SEND_STRING(QMK_KEYBOARD "/" QMK_KEYMAP " @ " QMK_VERSION);
           return false;
           break;
        case EPRM:
           if (record->event.pressed) {
                eeconfig_init();
           }
           return false;
           break;

        case SHELL_LSB:
           if (record->event.pressed) {
                /*SEND_STRING("lsb\n");*/
                send_unicode_string("(ノಠ痊ಠ)ノ彡┻━┻ 🔥");
           }
           break;

        case SHELL_LS:
           if (record->event.pressed) {
                /*send_unicode_string("(ノಠ痊ಠ)ノ彡┻━┻ 🔥");*/
                send_unicode_string("🐄");
                wait_ms(200);
                send_unicode_string("🐄");
                wait_ms(200);
                SEND_STRING("   ");
           }

     }
    return true;
}

